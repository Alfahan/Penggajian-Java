    private void prosesLogin() {
        String kd = tKdUser.getText();
        String pwd = pswd.getText();
        try {
            String sql = "select * from user where id_user='" + kd + "'"
                    + "and password='" + pwd + "'";
            kon.rs = kon.st.executeQuery(sql);

            if (kon.rs.next()) {
                String nama = kon.rs.getString("nm_user");
                JOptionPane.showMessageDialog(null, "Selamat Datang " + nama + "");
                if (lAkses.getText().equals("Admin")) {
                    menuUtama ma = new menuUtama();
                    gantiPassword gp = new gantiPassword();
                    ma.setLocationRelativeTo(null);
                    ma.setVisible(true);
                    ma.namaUser = kon.rs.getString("nm_user");
                    ma.kodeUser = kon.rs.getString("id_user");
                    ma.hakAkses = kon.rs.getString("level");

                    gp.Kode_User = kon.rs.getString("id_user");
                    this.dispose();
                } else {
                    menuUtama ma = new menuUtama();
                    gantiPassword gp = new gantiPassword();
                    ma.setLocationRelativeTo(null);
                    ma.setVisible(true);
                    ma.namaUser = kon.rs.getString("nm_user");
                    ma.kodeUser = kon.rs.getString("id_user");
                    ma.hakAkses = kon.rs.getString("level");

                    gp.Kode_User = kon.rs.getString("id_user");
                    this.dispose();
                }

            } else {
                JOptionPane.showMessageDialog(null, "Koode atau Password SALAH");
                bersih();
                tKdUser.setEnabled(true);
                tKdUser.requestFocus();
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

