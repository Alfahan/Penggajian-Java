    private void tKdUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tKdUserActionPerformed
        // TODO add your handling code here:
        String kodeUser = tKdUser.getText();
        try {
            String sql = "select * from user where id_user='" + kodeUser + "'";
            kon.rs = kon.st.executeQuery(sql);
            if (kon.rs.next()) {
                tKdUser.setEnabled(false);
                lAkses.setText(kon.rs.getString("level"));
                pswd.setEnabled(true);
                pswd.requestFocus();
                btMasuk.setEnabled(true);
                tKdUser.setEnabled(false);
            } else {
                JOptionPane.showMessageDialog(null, "Kode User Tidak Ditemukan");
                bersih();
                tKdUser.setEnabled(true);
                tKdUser.requestFocus();
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_tKdUserActionPerformed

