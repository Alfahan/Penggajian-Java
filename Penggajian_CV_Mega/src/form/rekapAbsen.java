    private void masukKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_masukKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        if (!((c >= '0') && (c <= '9') && masuk.getText().length() < 11
                || (c == com.sun.glass.events.KeyEvent.VK_BACKSPACE)
                || (c == com.sun.glass.events.KeyEvent.VK_DELETE))) {
            getToolkit().beep();
            evt.consume();
        }
    }//GEN-LAST:event_masukKeyTyped

